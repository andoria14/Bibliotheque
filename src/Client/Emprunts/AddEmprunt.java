    private void jTextFieldSearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSearchKeyTyped
        // TODO add your handling code here:
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            String search = jTextFieldSearch.getText();
            if (jRadioButtonLivre.isSelected()) {
                this.searchInLivres(search);
                this.selectedL = null;
            }
            if (jRadioButtonAdherent.isSelected()) {
                this.searchInAdherent(search);
                this.selectedA = null;
            }
        }
    }//GEN-LAST:event_jTextFieldSearchKeyTyped

    private void jListLivreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListLivreMouseClicked
        // TODO add your handling code here:
        Livre livre = (Livre) jListLivre.getSelectedValue();
        if (livre.isDisponibilite()) {
            jLabelRecap.setText("");
            if (!jListAdherent.isSelectionEmpty()) {
                Adherent adherent = (Adherent) jListAdherent.getSelectedValue();
                jLabelRecap.setText(livre.toString() + " va être emprunté par " + adherent.toString());
                this.selectedA = adherent;
            } else {
                jLabelRecap.setText(livre.toString() + " va être emprunté par ...");
            }
            this.selectedL = livre;
        }else{
            jLabelRecap.setText("<html><body><font color='red'>Le livre n'est pas disponible</font></body></html>");
        }
    }//GEN-LAST:event_jListLivreMouseClicked

    private void jListAdherentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListAdherentMouseClicked
        // TODO add your handling code here:
        Adherent adherent = (Adherent) jListAdherent.getSelectedValue();
        if (!jListLivre.isSelectionEmpty()) {
            Livre livre = (Livre) jListLivre.getSelectedValue();
            jLabelRecap.setText(livre.toString() + " va être emprunté par " + adherent.toString());
            this.selectedL = livre;
        } else {
            jLabelRecap.setText("... va être emprunté par " + adherent.toString());
        }
        this.selectedA = adherent;
    }//GEN-LAST:event_jListAdherentMouseClicked

    private void jButtonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValiderActionPerformed
        // TODO add your handling code here:
        if ((!jListLivre.isSelectionEmpty()) && (!jListAdherent.isSelectionEmpty())) {
            try {
                Emprunt emprunt = empruntMetierService.newEmprunt(selectedL, selectedA);
                emprunt.setDateEmprunt(new Date());
                emprunts.addEmprunt(emprunt);
                MetierServiceFactory.getBibliotheque().getTab().setStatut("<html><body><font color='green'>L'emprunt a été ajouté</font></body></html>", "emprunt");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(AddEmprunt.class.getName()).log(Level.SEVERE, null, ex);
                jLabelStatut.setText("<html><body><font color='red'>" + ex.getMessage() + "</font></body></html>");
            }
        }
    }//GEN-LAST:event_jButtonValiderActionPerformed

