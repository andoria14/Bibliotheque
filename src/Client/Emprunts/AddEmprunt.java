    private void searchInLivres(String search) {
        if (!search.equals("")) {
            try {
                StringTokenizer st = new StringTokenizer(search, " ");
                if (st.countTokens() != 0) {
                    List<String> list = new ArrayList<>();
                    for (int i = 0; i < st.countTokens() + 1; i++) {
                        list.add(st.nextToken());
                    }
                    List<Livre> byMotsClefs = livreMetierService.getByMotsClefs(list);
                    Vector<Livre> vector = new Vector<>(byMotsClefs);
                    jListLivre.setListData(vector);
                }
            } catch (Exception ex) {
                Logger.getLogger(AddEmprunt.class.getName()).log(Level.SEVERE, null, ex);
                jLabelStatut.setText("<html><body><font color='red'>" + ex.getMessage() + "</font></body></html>");
            }
        }
    }

    private void searchInAdherent(String search) {
        if (!search.equals("")) {
            try {
                List<Adherent> byNom = adherentMetierService.getByNom(search);
                Vector<Adherent> vector = new Vector<>(byNom);
                jListAdherent.setListData(vector);
            } catch (Exception ex) {
                Logger.getLogger(AddEmprunt.class.getName()).log(Level.SEVERE, null, ex);
                jLabelStatut.setText("<html><body><font color='red'>" + ex.getMessage() + "</font></body></html>");
            }
        }
    }

