    private void jPasswordFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordFieldKeyTyped
        // TODO add your handling code here:
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
             try {
                char[] mdptab = jPasswordField.getPassword();
                StringBuilder sb = new StringBuilder();
                for (int i = 0; i < mdptab.length; i++) {
                    sb.append(mdptab[i]);
                }
                String mdp = sb.toString();

                Bibliothecaire tmp = MetierServiceFactory.getAdherentService().newBibliothecaire(bibliothecaire.getNom(), bibliothecaire.getPrenom(), bibliothecaire.getLogin(), mdp, false);
                tmp.setId(bibliothecaire.getId());
                if (tmp.equals(bibliothecaire)) {
                    try {
                        bibliothecaires.removeBiblio(bibliothecaire);
                        if(bibliothecaire.equals(MetierServiceFactory.getBibliotheque().getBibliothecaireConnecte())) {
                            MetierServiceFactory.getBibliotheque().deconnecter();
                        }
                        this.dispose();
                    } catch (Exception ex) {
                        Logger.getLogger(ConfSupprWithPass.class.getName()).log(Level.SEVERE, null, ex);
                        jLabelStatut.setText("<html><body><font color='red'>" + ex.getMessage() + "</font></body></html>");
                    }
                } else {
                    jLabelStatut.setText("<html><body><font color='red'>Mot de passe incorrect</font></body></html>");
                }
            } catch (Exception ex) {
                Logger.getLogger(ConfSupprWithPass.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jPasswordFieldKeyTyped

